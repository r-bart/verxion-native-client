/** es-ES slug, matching the handoff's `rdSlug` (links to the exercise detail). */
function slug(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

// [name, muscle, equipment, sets, repRange, rir, rest, target, lastResult, progression|null, isKey]
type Row = [
  string,
  string,
  string,
  number,
  string,
  number,
  string,
  string,
  string,
  string | null,
  boolean
];

function toItems(rows: Row[]): DayExerciseItem[] {
  return rows.map(
    (
      [
        name,
        muscle,
        equipment,
        sets,
        repRange,
        rir,
        rest,
        target,
        lastResult,
        progression,
        isKey,
      ],
      i
    ) => ({
      exerciseId: slug(name),
      index: i + 1,
      name,
      isKey,
      muscle,
      equipment,
      target,
      sets,
      repRange,
      rir,
      rest,
      lastResult,
      progression,
    })
  );
}

