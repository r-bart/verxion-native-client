function build(seed: DaySeed): DayDetailView {
  const exercises = toItems(seed.rows);
  return {
    header: {
      dayId: seed.dayId,
      type: seed.type,
      routineName: "PPL Hipertrofia",
      dayNumber: seed.dayNumber,
      trainingTotal: 6,
      name: seed.name,
      dayOfWeek: seed.dayOfWeek,
      focus: seed.focus,
      exercisesCount: exercises.length,
      setsCount: exercises.reduce((a, e) => a + e.sets, 0),
      estimateMin: seed.estimateMin,
      volumeEstimate: VOL_EST[seed.type] ?? "—",
    },
    isRest: false,
    restFocus: null,
    agentNote: NOTE[seed.type] ?? null,
    exercises,
  };
}

export const dayDetailFixtures: Record<string, DayDetailView> =
  Object.fromEntries(SEEDS.map((s) => [s.dayId, build(s)]));

/** Stub resolver: returns the day detail for `id`, falling back to the today's day. */
export function dayDetailFixtureFor(id: string): DayDetailView {
  return dayDetailFixtures[id] ?? dayDetailFixtures["legs-a"];
}
